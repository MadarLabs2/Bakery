export type AccessibilityDictKey = keyof typeof accessibilityDict;

export function formatAccessibility(
  template: string,
  vars: Record<string, string>,
): string {
  let result = template;
  for (const [key, value] of Object.entries(vars)) {
    result = result.replaceAll(`{{${key}}}`, value);
  }
  return result;
}

export function accessibilityT(
  key: AccessibilityDictKey,
  lang: Lang,
  vars?: Record<string, string>,
): string {
  const entry = accessibilityDict[key];
  const raw = entry[lang] ?? entry.en;
  return vars ? formatAccessibility(raw, vars) : raw;
}

export function accessibilityList(
  keys: readonly AccessibilityDictKey[],
  lang: Lang,
  vars?: Record<string, string>,
): string[] {
  return keys.map((key) => accessibilityT(key, lang, vars));
}
