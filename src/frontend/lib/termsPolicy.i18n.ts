export type TermsDictKey = keyof typeof termsDict;

export function formatTerms(
  template: string,
  vars: Record<string, string>,
): string {
  let result = template;
  for (const [key, value] of Object.entries(vars)) {
    result = result.replaceAll(`{{${key}}}`, value);
  }
  return result;
}

export function termsT(
  key: TermsDictKey,
  lang: Lang,
  vars?: Record<string, string>,
): string {
  const entry = termsDict[key];
  const raw = entry[lang] ?? entry.en;
  return vars ? formatTerms(raw, vars) : raw;
}

export function termsList(
  keys: readonly TermsDictKey[],
  lang: Lang,
  vars?: Record<string, string>,
): string[] {
  return keys.map((key) => termsT(key, lang, vars));
}
