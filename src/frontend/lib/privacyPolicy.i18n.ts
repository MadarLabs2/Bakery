export type PrivacyDictKey = keyof typeof privacyDict;

export function formatPrivacy(
  template: string,
  vars: Record<string, string>,
): string {
  let result = template;
  for (const [key, value] of Object.entries(vars)) {
    result = result.replaceAll(`{{${key}}}`, value);
  }
  return result;
}

export function privacyT(
  key: PrivacyDictKey,
  lang: Lang,
  vars?: Record<string, string>,
): string {
  const entry = privacyDict[key];
  const raw = entry[lang] ?? entry.en;
  return vars ? formatPrivacy(raw, vars) : raw;
}

export function privacyList(
  keys: readonly PrivacyDictKey[],
  lang: Lang,
  vars?: Record<string, string>,
): string[] {
  return keys.map((key) => privacyT(key, lang, vars));
}
